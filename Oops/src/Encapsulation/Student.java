package Encapsulation;

public class Student 
{
String name;
int id;
String dep;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getDep() {
	return dep;
}
public void setDep(String dep) {
	this.dep = dep;
}
}


